# There are two kinds of operators in Ruby, which can be both used
# inside properties, lazy and non-lazy:

# Lazy operators
#
# Logical conjunction: and, &&
# Logical disjunction: or, ||
#
# Lazy operators are special syntax, they are not methods.

# Non-lazy operators
#
# Logical conjunction: &
# Logical disjunction: |
#
# Non-lazy operators are implemented by class methods.
